package dto;

public class UsuarioDTO {
	int idUsuario;
	String nickUsuario;
	String claveUsuario;
	String nombreCompletoUsuario;
	String documentoUsuario;
	String nacionalidadUsuario;
	String direccionUsuario;
	String telefonoMovilUsuario;
	String telefonoFijoUsuario;
	
	public int getIdUsuario() {
		return idUsuario;
	}
	
	public void setIdUsuario(int idUsuario) {
		this.idUsuario = idUsuario;
	}
	
	public String getNickUsuario() {
		return nickUsuario;
	}
	
	public void setNickUsuario(String nickUsuario) {
		this.nickUsuario = nickUsuario;
	}
	
	public String getClaveUsuario() {
		return claveUsuario;
	}
	
	public void setClaveUsuario(String claveUsuario) {
		this.claveUsuario = claveUsuario;
	}
	
	public String getNombreCompletoUsuario() {
		return nombreCompletoUsuario;
	}
	
	public void setNombreCompletoUsuario(String nombreCompletoUsuario) {
		this.nombreCompletoUsuario = nombreCompletoUsuario;
	}
	
	public String getDocumentoUsuario() {
		return documentoUsuario;
	}
	
	public void setDocumentoUsuario(String documentoUsuario) {
		this.documentoUsuario = documentoUsuario;
	}
	
	public String getNacionalidadUsuario() {
		return nacionalidadUsuario;
	}
	
	public void setNacionalidadUsuario(String nacionalidadUsuario) {
		this.nacionalidadUsuario = nacionalidadUsuario;
	}
	
	public String getDireccionUsuario() {
		return direccionUsuario;
	}
	
	public void setDireccionUsuario(String direccionUsuario) {
		this.direccionUsuario = direccionUsuario;
	}
	
	public String getTelefonoMovilUsuario() {
		return telefonoMovilUsuario;
	}
	
	public void setTelefonoMovilUsuario(String telefonoMovilUsuario) {
		this.telefonoMovilUsuario = telefonoMovilUsuario;
	}
	
	public String getTelefonoFijoUsuario() {
		return telefonoFijoUsuario;
	}
	
	public void setTelefonoFijoUsuario(String telefonoFijoUsuario) {
		this.telefonoFijoUsuario = telefonoFijoUsuario;
	}	
}