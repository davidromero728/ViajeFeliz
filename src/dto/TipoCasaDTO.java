package dto;

public class TipoCasaDTO {
	int idTipoCasa;
	String nombreTipoCasa;
	
	public int getIdTipoCasa() {
		return idTipoCasa;
	}
	
	public void setIdTipoCasa(int idTipoCasa) {
		this.idTipoCasa = idTipoCasa;
	}
	
	public String getNombreTipoCasa() {
		return nombreTipoCasa;
	}
	
	public void setNombreTipoCasa(String nombreTipoCasa) {
		this.nombreTipoCasa = nombreTipoCasa;
	}
}